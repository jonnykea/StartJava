package Variable;
// Вывод на консоль слова JAVA
public class VariableTheme3 {
    public static void main(String[] args) {
        String a = "a";
        String b = "v";
        String c = "V";
        String d = "J";
        System.out.println(" " + d + "  " + a + " " + b + "  " + b + " " + a);
        System.out.println(" " + d + "  " + a + " " + a + " " + b + "  " + b + " " + a + " " + a);
        System.out.println(d + " " + d + "  " + a + a + a + a + "  " + c + " " + c + " " + a + a + a + a);
        System.out.println(" " + d + d + "   " + a + "   " + a + " " + c + " " + a + "   " + a);
    }
}
